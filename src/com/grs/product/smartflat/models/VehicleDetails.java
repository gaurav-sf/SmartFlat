package com.grs.product.smartflat.models;

public class VehicleDetails {
	
	private String mVehicleType;	
	private String mVehicleNumber;	
	private String mVehicleCompany;
	private String mVehicleModel;	
	private String mVehicleColor;
	
	public String getmVehicleType() {
		return mVehicleType;
	}
	
	public void setmVehicleType(String mVehicleType) {
		this.mVehicleType = mVehicleType;
	}
	
	public String getmVehicleNumber() {
		return mVehicleNumber;
	}
	
	public void setmVehicleNumber(String mVehicleNumber) {
		this.mVehicleNumber = mVehicleNumber;
	}
	
	public String getmVehicleCompany() {
		return mVehicleCompany;
	}
	
	public void setmVehicleCompany(String mVehicleCompany) {
		this.mVehicleCompany = mVehicleCompany;
	}
	
	public String getmVehicleModel() {
		return mVehicleModel;
	}
	
	public void setmVehicleModel(String mVehicleModel) {
		this.mVehicleModel = mVehicleModel;
	}
	
	public String getmVehicleColor() {
		return mVehicleColor;
	}
	
	public void setmVehicleColor(String mVehicleColor) {
		this.mVehicleColor = mVehicleColor;
	}	

}
