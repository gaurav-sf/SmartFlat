package com.grs.product.smartflat.models;

public class ComplaintDetails {
	
	private String mComplaintNumber;	
	private String mComplaintType;	
	private String mComplaintStatus;	
	private String mComplaintRaisedDateTime;	
	private String mComplaintDetails;
	
	public String getmComplaintNumber() {
		return mComplaintNumber;
	}
	
	public void setmComplaintNumber(String mComplaintNumber) {
		this.mComplaintNumber = mComplaintNumber;
	}
	
	public String getmComplaintType() {
		return mComplaintType;
	}
	
	public void setmComplaintType(String mComplaintType) {
		this.mComplaintType = mComplaintType;
	}
	
	public String getmComplaintStatus() {
		return mComplaintStatus;
	}
	
	public void setmComplaintStatus(String mComplaintStatus) {
		this.mComplaintStatus = mComplaintStatus;
	}
	
	public String getmComplaintRaisedDateTime() {
		return mComplaintRaisedDateTime;
	}
	
	public void setmComplaintRaisedDateTime(String mComplaintRaisedDateTime) {
		this.mComplaintRaisedDateTime = mComplaintRaisedDateTime;
	}
	
	public String getmComplaintDetails() {
		return mComplaintDetails;
	}
	
	public void setmComplaintDetails(String mComplaintDetails) {
		this.mComplaintDetails = mComplaintDetails;
	}

}
